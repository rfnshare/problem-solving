#include<stdio.h>

int main()
{
    int m;
    printf("Sample Non Negative Input:\n");
    scanf("%d", &m);
    if(0<m)
    {
        printf("The value of n = 1");
    }
    else if(m==0)
    {
        printf("The value of n = 0");
    }
    else
    {
        printf("The value of n = -1");
    }



    return 0;
}


